Pada tanggal 17 Agustus 1945, Pimpinan Kepolisian di daerah – daerah termasuk di Daerah Nusa Tenggara melepaskan diri dari kekuasaan pimpinan Jepang dan menyatakan dirinya sebagai bagian dari Polisi Republik Indonesia. <br />
Pada tanggal 24 Desember 1946 berlakulah Ordenantie No. 143 tahun 1946 dari Pimpinan Indonesia Timur tentang “ Susunan dan Bentuk–bentuk Pemerintahan “ yang didasarkan atas peraturan Swapraja tahun 1938, dimana organisasi Kepolisian di daerah – daerah mengikuti bentuk tersebut.
Kepolisian dalam lingkungan Pemerintah Negara Indonesia Timur, secara administratif dibawah Menteri Dalam Negeri Negara Indonesia Timur dan dibantu oleh seorang Kepala Jawatan Kepolisian Negara Indonesia Timur. <br/> Daerah Nusa Tenggara pada saat itu dibagi menjadi 2 Daerah Kepolisian yaitu :
<br />1.Kepolisian Keresidenan Bali dan Lombok berkedudukan di Denpasar yang membawahi Kepolisian pada tiap-tiap Swapraja di Pulau Bali dan Lombok.
<br />2.Kepolisian Keresidenan Timur dan Kepulauannya berkedudukan di Kupang yang membawahi Kepolisian pada tiap - tiap Swapraja pada kepulauan tersebut.</span>
<br />Kemudian organisasi Kepolisian yang ada di daerah Nusa Tenggara mengalami perubahan menjadi 5 (Lima) daerah yang masing – masing dikepalai oleh Kepala Kepolisian Daerah (KPDA). Kelima daerah tersebut yaitu : <br />
a. Kepolisian Daerah Bali berkedudukan di Denpasar. <br />
b. Kepolisian Daerah Lombok berkedudukan di Ampenan. <br />
c. Kepolisian Daerah Sumbawa /Sumba berkedudukan di Bima.<br />
d. Kepolisian daerah Plores berkedudukan di Ende.<br />
e. Kepolisian Daerah Timor berkedudukan di Kupang.<br />
Pada tanggal 17 Agustus 1950 terbentuklah Negara Kesatuan Republik Indonesia, kemudian lahir pula Jawatan Kepolisian Negara pada 19 Agustus 1950, dimana masing-masing Pimpinan Kepolisian di daerah berhubungan langsung dengan Kepolisian Negara di Jakarta.</p>
